def entry_for_version(version):
    return next((entry for entry in CHANGELOG if entry['version'] == version), None)


def notes_for_version(version):
    entry = entry_for_version(version)
    if entry is None:
        raise ValueError(f'更新日志中缺少版本 {version}')
    parts = [entry['summary'], *entry['items']]
    return '；'.join(str(part).rstrip('。；; ') for part in parts) + '。'
